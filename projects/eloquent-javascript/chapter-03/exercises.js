// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
// creating function called countChars that takes in the parameters of a string and a character 
function countChars(string, char) {
  let count = 0; // creating a count varriable 
  for(i = 0; i < string.length; i++){ // creating a for loop to iterate through string parameter 
    if(string[i] === char){ // if the element of string equals char parameter count goes up by 1 
      count += 1; 
    }
  }
  return count; // return count

}

